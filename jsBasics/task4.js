// Ï€ (pi) value
const PI = Math.PI;

// Declare a variable for radius
let radius = 5;

// Calculate the area of the circle
let circleArea = (PI * Math.pow(radius, 2)).toFixed(2); // Rounded to 2 decimal places
console.log("Area of Circle:", circleArea);

// task4.2.js

// Declare variables for length and width
let length = 10;
let width = 4;

// Calculate the area of the rectangle
let rectangleArea = length * width; // Rounded to 2 decimal places
console.log("Area of Rectangle:", rectangleArea);


// task4.3.js

// Declare a variable for cylinder radius and height
let cylinderRadius = 3;
let cylinderHeight = 7;

// Calculate the volume of the cylinder
let cylinderVolume = (PI * Math.pow(cylinderRadius, 2) * cylinderHeight).toFixed(2); // Rounded to 2 decimal places
console.log("Volume of Cylinder:", cylinderVolume);
