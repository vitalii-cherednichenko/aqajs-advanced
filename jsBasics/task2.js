// Declare variables for names
let person1 = "Alice";
let person2 = "Vitalii";

// Concatenate strings to create a greeting
let greetingConcatenation = "Hello, " + person1 + " and " + person2 + "," + " nice to meet you!";
console.log("Greeting (concatenation):", greetingConcatenation);

// Use template literals to create the same greeting
let greetingTemplate = `Hello, ${person1} and ${person2}, nice to meet you!`;
console.log("Greeting (template literal):", greetingTemplate);
